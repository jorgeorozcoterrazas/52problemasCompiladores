package problema4;
//4.-Hacer un programa que imprima la suma de los 100 primeros numeros.
/**
 *
 * @author Jorge
 */
public class NewMain {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int z=0;
        for (int i = 0; i<= 100; i++) {
            
            z=z+i;
        }
        System.out.println(z);
    }
    
}
