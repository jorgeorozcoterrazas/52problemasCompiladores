package problema2;
//2.-Hacer un pseudocodigo que imprima los numeros del 100 al 0, en orden
//decreciente.
/**
 *
 * @author Jorge
 */
public class problema2 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        for (int i = 100; i>= 0; i--) {
            System.out.println(i);
        }
    }
    
}
